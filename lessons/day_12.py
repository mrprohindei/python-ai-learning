# --- Задача A ---
# Спроси температуру (целое число).
# Если temp < -30 ИЛИ temp > 40 — выведи: Экстремальная температура!
# Иначе — выведи: Погода в норме.
n = int(input("Какая температура на улице?: "))
if n < -30 or n>40:
    print("Экстремальная температура!")
else:
    print("Погода в норме.")

# --- Задача B ---
# Спроси логин и пароль (две строки через input).
# Если логин == "admin" И пароль == "123" — выведи: Доступ разрешён
# Иначе — выведи: Доступ запрещён
n = input("Введите логин: ")
x = input("Введите пароль: ")
while n != "admin" or x != "123":
    print("Неверный логин или пароль, повторите ввод")
    n = input("Введите логин: ")
    x = input("Введите пароль: ")
print("Доступ разрешен")


# --- Задача C (опционально) ---
# Спроси целое число.
# Если число меньше 1 ИЛИ больше 100 — выведи: Число вне диапазона 1–100
# Иначе — выведи: Число принято: ... (подставь введённое)
n = int(input("Введитте целое число от 1-го до 100: "))
while n<1 or n>100:
    print("Число вне диапазона 1–100")
    n = int(input("Введитте целое число от 1-го до 100: "))
print("Число принято:", n)

