# --- Мини-проект ---
# Сделай калькулятор в терминале.
#
# Программа должна:
# 1. Спросить два целых числа (a и b).
# 2. Спросить операцию: +, -, *, /  (пользователь вводит символ).
# 3. Выполнить нужное действие и вывести результат.
# 4. Если выбрано деление (/) и b равно 0 — вывести сообщение об ошибке,
#    а не делить (на ноль делить нельзя).
# 5. Если операция неизвестна (не +, -, *, /) — сообщить об этом (ветка else).
#
# Используй: input, int, print, if / elif / else, арифметику.
# Цикл for — не обязателен (но можно для опционального улучшения).
#
# Пример работы:
#   Первое число: 10
#   Второе число: 3
#   Операция (+, -, *, /): +
#   Результат: 10 + 3 = 13


# --- Опционально (если базовый калькулятор готов) ---
# Добавь цикл for: выведи таблицу умножения на 7 для чисел от 1 до 10
# (7 × 1 = 7, 7 × 2 = 14, …).
n_1 = int(input("Введите первое число - "))
n_2 = int(input("Введите второе число - "))
op = input("Выберите действие +, -, *, / или ?(подсчет суммы чисел выбранного диапазаона) - ")
x=0
if op == "+":
    z = n_1 + n_2
    print("Сумма:", n_1, "+", n_2, "=", z)
elif op == "-":
    z = n_1 - n_2
    print("Вычитание:", n_1, "-", n_2, "=", z)
elif op == "*":
    z = n_1 * n_2
    print("Умножение:", n_1, "*", n_2, "=", z)
elif op == "/":
    if n_2 == 0:
        print("На ноль делить нельзя!")
    else:
        z = n_1 / n_2
        print("Деление:", n_1, "/", n_2, "=", z)
elif op == "?":
    if n_1 < n_2:
        for i in range(n_1, n_2+1):
            x=x+i
        print ("Сумма чисел выбранного диапазона равна", x)
    elif n_1 > n_2:
        print("Для подсчета суммы диапазона, первое число не моежт быть больше второго!")

else:
    print("Выберите одно из 5-ти действий!")


