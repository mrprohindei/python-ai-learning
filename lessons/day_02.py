# --- Задача A ---
# 1. Спроси у пользователя имя:  input("Как тебя зовут? ")
# 2. Спроси город.
# 3. Сохрани ответы в переменные name и city.
# 4. Выведи одну строку:
#    Рад знакомству, Сергей из Норильска!
#    (со своими данными, которые ввёл пользователь)
name = input("как тебя зовут?")
city = input("из какого ты города?")
print("Рад знакомству,", name, "из", city + "!")

# --- Задача B ---
# 1. Спроси любимый цвет.
# 2. Сохрани в переменную color.
# 3. Выведи:
#    Твой любимый цвет — синий. Запомнил!
#    (цвет — тот, что ввёл пользователь)
color = input("Какой твой любимый цвет?")
print("Твой любимый цвет -", color + ". Запомнил!")