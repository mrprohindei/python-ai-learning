# --- Задача A ---
# Спроси у пользователя любую фразу (строку).
# Выведи: Длина: N  (N — число символов, используй len).
x = input("Введиет строку - ")
print("Длина:",len(x))

# --- Задача B ---
# Спроси: Продолжить? (да/нет)
# Приведи ответ к нижнему регистру (.lower()) и убери пробелы (.strip()).
# Если после этого "да" — выведи: Продолжаем!
# Если "нет" — выведи: Остановлено.
# Иначе — выведи: Не понял ответ.
x = input("Продолжить? (да/нет):" )
if x.strip().lower() == "да":
    print("Продолжаем!")
elif x.strip().lower() == "нет":
    print("Остановлено")
else:
    print("Не понял ответ")



# --- Задача C (опционально) ---
# Спроси строку.
# Циклом for посчитай, сколько раз в ней встречается буква «а» (маленькая).
# Выведи: Буква «а» встречается: N раз
f = input("Введите строку: ")
x=0
for i in f:
    if i == "а":
        x = x + 1
print("Буква «а» встречается:",x)
    

