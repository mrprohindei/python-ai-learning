# --- Задача A ---
# Циклом for выведи числа от 1 до 10 включительно — каждое с новой строки.
# Используй range. Проверь: первое число 1, последнее 10.
for i in range(1,11):
    print(i)

# --- Задача B ---
# Спроси у пользователя целое число N (через input и int).
# Циклом посчитай сумму всех чисел от 1 до N включительно.
# Выведи результат одной строкой, например:
#   Сумма от 1 до 5 = 15
# (подставь своё N и правильную сумму)
x = int(input("Введите число - "))
z = 0
for i in range(1,x+1):
    z = z + i
print ("Сумма чисел от 1 до", x, "=", z)
   


# --- Задача C (опционально) ---
# Спроси у пользователя целое число N.
# Циклом выведи фразу «Учусь Python!» ровно N раз — каждый раз с новой строки.
# Подсказка из теории: если номер шага не нужен, можно for _ in range(...):
u = int(input("Введите число - "))
for _ in range(u):
    print("Учусь Python!")