# --- Задача A ---
# Спроси у пользователя целое число N.
# Циклом перебери числа от 1 до N включительно.
# Выведи только те, которые **больше 5** (каждое с новой строки).
# Если N <= 5 — может ничего не напечататься, это нормально.
n = int(input("Введите число N: "))
for i in range(1,n+1):
    if i > 5:
        print(i)
    else:
        print(i,"Это число меньше 5")

# --- Задача B ---
# Спроси у пользователя целое число N.
# Циклом посчитай **произведение** всех чисел от 1 до N включительно.
# Пример: N = 4 → 1 × 2 × 3 × 4 = 24
# Выведи одной строкой, например:
#   Произведение от 1 до 4 = 24
# Подсказка из теории: для умножения стартовое значение — не ноль.
z = int(input("Введите число N: "))
count = 1
for i in range(1,z+1):
    count = count * i
print("Произведение от 1 до", z, "=", count)

# --- Задача C (опционально) ---
# Без input: циклом выведи все **чётные** числа от 2 до 20 включительно.
# Используй for, if и оператор % (остаток от деления).
for i in range(1, 20+1):
    if i % 2 == 0:
        print(i)
