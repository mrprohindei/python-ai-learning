# --- Задача A ---
# Спроси у пользователя целое число N (N >= 1).
# Циклом **while** (не for!) выведи числа от 1 до N включительно —
# каждое с новой строки.
#
# Пример при N = 4:
#   1
#   2
#   3
#   4
#
# Подумай: с какого числа начать, что проверять в условии,
# что менять на каждом шаге.
n = int(input("Введите число N - "))
x = 1
while x <= n:
    print(x)
    x=x+1

# --- Задача B ---
# Спроси у пользователя возраст (целое число).
# Пока возраст меньше 1 или больше 120 — выводи
# «Возраст должен быть от 1 до 120» и спрашивай снова.
# Когда возраст в диапазоне — выведи: Возраст принят: ...
n = int(input("Ввдите ваш возраст -"))
while n>120 or n<1:
    print("Введите корректный возвраст!")
    n = int(input("Ввдите ваш возраст -"))
print("Возраст принят!")

# --- Задача C (опционально) ---
# Спрашивай целые числа по одному.
# Считай, **сколько** чисел ввёл пользователь, пока не введёт 0.
# Сам ноль не считай. В конце выведи: Введено чисел: ...
# Пример: 5, 3, 8, 0 → Введено чисел: 3
n = int(input("Введите числа для подсчета. 0 - конец ввода: "))
x = 0
while n != 0:
    print("Введенное число: ", n)
    n = int(input("Введите следующее число: "))
    x = x + 1
print("Количество введенных чисел:", x)


