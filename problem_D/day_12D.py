# --- Задача ---
# Одна программа «Мини-заказ».
#
# 1. Спроси количество товара (целое) и цену за штуку (целое).
# 2. Пока количество <= 0 ИЛИ цена <= 0:
#      выведи «Количество и цена должны быть больше нуля»
#      и спроси оба значения заново.
# 3. Когда оба значения положительные — посчитай итог (количество * цена)
#    и выведи: Сумма заказа: ...
#
# Продумай сам: что спрашивать до while, что внутри, когда выходить.
n = int(input("Введите количество товаров: "))
x = int(input("Укажите цену за штуку "))
while n <= 0 or x <= 0:
    print("Количество и цена должны быть больше нуля!")
    n = int(input("Введите количество товаров: "))
    x = int(input("Укажите цену за штуку "))
s = n * x
print("Сумма заказа:", s)