# --- Задача ---
# Одна программа «Мини-статистика чисел».
#
# Спрашивай целые числа по одному, пока пользователь не введёт 0.
#
# Для каждого числа (кроме нуля):
#   — если отрицательное: выведи «Пропущено: отрицательное» и не учитывай в суммах;
#   — если положительное и чётное: прибавь к sum_even;
#   — если положительное и нечётное: прибавь к sum_odd.
#
# Ноль — только сигнал остановки, в суммы не входит.
#
# В конце выведи три строки:
#   Сумма чётных: ...
#   Сумма нечётных: ...
#   Учтено чисел: ...   (сколько положительных обработано)
#
# Пример ввода: 2, -1, 3, 4, 0
#   sum_even = 6  (2 + 4), sum_odd = 3, учтено = 3
#
# Продумай сам: какие переменные нужны до цикла, что спрашивать в while,
# где поставить if / elif / else.
n = int(input("Введите целое число: "))
summ_even = 0
summ_odd = 0
summ_all = 0
while n != 0:
    if n<0:
        print("Пропущено: отрицательное")
        n = int(input("Введите целое число: "))
    elif n % 2 == 0:
        summ_even = summ_even + n
        summ_all = summ_all +1
        n = int(input("Введите целое число: "))
    else:
        summ_odd = summ_odd + n
        summ_all = summ_all +1
        n = int(input("Введите целое число: "))
print("Сумма четный числе: ", summ_even, " Сумма нечетных числе: ", summ_odd, " Количество всех положительных чисел: ", summ_all )

