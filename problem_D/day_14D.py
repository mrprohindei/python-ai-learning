# --- Задача ---
# Одна программа. Минимум **две функции** — сам реши какие.
#
# Сценарий:
# 1. Спросить логин и пароль (строки).
# 2. Логин после .strip().lower() должен быть "user".
#    Пароль после .strip() должен быть "12345" (регистр у пароля важен).
# 3. Пока логин короче 3 символов (до обработки или после strip — как решишь) —
#    сообщение и повторный ввод логина.
# 4. Пока пароль короче 5 символов — сообщение и повторный ввод пароля.
# 5. Если логин и пароль верные — «Добро пожаловать!»
#    Иначе — «Неверный логин или пароль»
#
# Подсказка: результат функции return лови в переменную (как в 13D).
def data(name):
    while len(name.strip().lower()) < 3:
        name = input("Логин не должен быть короче 3-х символов: ")
    return name.strip().lower()

def data_2(pasw):
    while len(pasw.strip()) < 5 :
        pasw = input("Пароль короче 5 символов, повторите ввод: ")
    return pasw.strip()

x = input("Введите имя: ")
y = input("Введите пароль: ")
f = data(x)
z = data_2(y)

if f != "user" or z != "12345":
    print("Неверный логин или пароль")
else:
    print("Добро пожаловать!")



